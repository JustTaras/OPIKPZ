# Введення двох чисел
a = float(input("Введіть перше число: "))
b = float(input("Введіть друге число: "))

# Арифметичні операції
print("Додавання:", a + b)
print("Віднімання:", a - b)
print("Множення:", a * b)

if b != 0:
    print("Ділення:", a / b)
else:
    print("Ділення неможливе (ділення на нуль)")

# Порівняння чисел
if a > b:
    print("Перше число більше за друге")
elif a < b:
    print("Перше число менше за друге")
else:
    print("Числа рівні")

# Робота з рядком
text = input("Введіть рядок: ")
length = len(text)

print("Довжина рядка:", length)

if length < 5:
    print("Рядок короткий")
elif 5 <= length <= 10:
    print("Рядок середній")
else:
    print("Рядок довгий")

# Логічна перевірка числа на належність до діапазону
num = int(input("Введіть ціле число для перевірки діапазону: "))

if num >= 10 and num <= 20:
    print("Число знаходиться в діапазоні від 10 до 20")
else:
    print("Число не належить діапазону від 10 до 20")
